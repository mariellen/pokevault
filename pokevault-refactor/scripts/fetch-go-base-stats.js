#!/usr/bin/env node
// Generates js/pokemon_go_base_stats.js from PvPoke's public gamemaster data.
// Run: node scripts/fetch-go-base-stats.js
// Takes ~5 seconds (single HTTP request).

'use strict';

const fs   = require('fs');
const path = require('path');

const PVPOKE_URL  = 'https://raw.githubusercontent.com/pvpoke/pvpoke/master/src/data/gamemaster/pokemon.json';
const OUTPUT_PATH = path.join(__dirname, '..', 'js', 'pokemon_go_base_stats.js');

// ── CP Multipliers ────────────────────────────────────────────────────────────
// Integer-level CPMs data-mined from the GO game master file.
// Half-level CPMs derived using Niantic's formula: sqrt((L^2 + (L+1)^2) / 2)
const INTEGER_CPMS = {
   1: 0.09399999678,  2: 0.16639787,     3: 0.21573247,     4: 0.25572005,
   5: 0.29024988,     6: 0.32108760,     7: 0.34921573,     8: 0.37523559,
   9: 0.39956728,    10: 0.42250001,    11: 0.44413572,    12: 0.46450975,
  13: 0.48369673,    14: 0.50177455,    15: 0.51879072,    16: 0.53480815,
  17: 0.54990582,    18: 0.56414132,    19: 0.57755737,    20: 0.59740001,
  21: 0.61279070,    22: 0.62783647,    23: 0.64278126,    24: 0.65589491,
  25: 0.66999999,    26: 0.68154001,    27: 0.69306998,    28: 0.70453001,
  29: 0.71594999,    30: 0.72729999,    31: 0.73822999,    32: 0.74999999,
  33: 0.76116001,    34: 0.77192999,    35: 0.78234998,    36: 0.79200000,
  37: 0.80153999,    38: 0.81095999,    39: 0.82026999,    40: 0.83012001,
  41: 0.83499998,    42: 0.84489998,    43: 0.85264999,    44: 0.86039997,
  45: 0.86813999,    46: 0.87587997,    47: 0.88362001,    48: 0.89136001,
  49: 0.89909999,    50: 0.90686035,    51: 0.91500001,
};

function buildCpmTable() {
  const table = {};
  for (let lvl = 1; lvl <= 51; lvl++) {
    table[lvl] = INTEGER_CPMS[lvl];
    if (lvl < 51) {
      const half = Math.sqrt((Math.pow(INTEGER_CPMS[lvl], 2) + Math.pow(INTEGER_CPMS[lvl + 1], 2)) / 2);
      table[lvl + 0.5] = Math.round(half * 1e11) / 1e11;
    }
  }
  return table;
}

// ── Name normalisation ────────────────────────────────────────────────────────
// PvPoke speciesIds use lowercase + underscores for forms (e.g. "rattata_alolan").
// This produces a lookup key that matches for standard species names.
function normName(name) {
  return name.toLowerCase()
    .replace(/['.♀♂:é]/g, s => s === 'é' ? 'e' : '')
    .replace(/[\s\-]+/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '');
}

async function main() {
  console.log('Fetching PvPoke pokemon gamemaster…');
  const res = await fetch(PVPOKE_URL);
  if (!res.ok) throw new Error(`HTTP ${res.status} from ${PVPOKE_URL}`);
  const pokemon = await res.json();
  console.log(`  Received ${pokemon.length} entries.`);

  const byDex  = {};  // dex (int)  → {atk, def, sta}  — first entry per dex (base form)
  const byName = {};  // speciesId  → {atk, def, sta}   — all entries including forms

  for (const p of pokemon) {
    const { dex, speciesId, baseStats } = p;
    if (!dex || !speciesId || !baseStats) continue;
    const stats = { atk: baseStats.atk, def: baseStats.def, sta: baseStats.hp }; // PvPoke uses "hp" for stamina
    if (!(dex in byDex)) byDex[dex] = stats;  // first entry per dex = base form
    byName[speciesId] = stats;
  }

  console.log(`  Built: ${Object.keys(byDex).length} dex entries, ${Object.keys(byName).length} name entries.`);

  const cpmTable = buildCpmTable();

  // ── Serialise ───────────────────────────────────────────────────────────────
  const dexLines  = Object.entries(byDex)
    .sort(([a],[b]) => Number(a)-Number(b))
    .map(([k,v]) => `  ${k}:{atk:${v.atk},def:${v.def},sta:${v.sta}}`)
    .join(',\n');

  const nameLines = Object.entries(byName)
    .sort(([a],[b]) => a.localeCompare(b))
    .map(([k,v]) => `  '${k}':{atk:${v.atk},def:${v.def},sta:${v.sta}}`)
    .join(',\n');

  const cpmLines  = Object.entries(cpmTable)
    .sort(([a],[b]) => Number(a)-Number(b))
    .map(([k,v]) => `  ${k}:${v}`)
    .join(',\n');

  const header = `// Generated by scripts/fetch-go-base-stats.js — do not edit manually.
// Source: pvpoke/pvpoke gamemaster/pokemon.json  (${new Date().toISOString().slice(0,10)})
// GO base stats: atk/def/sta.  CP multipliers: levels 1–51 in 0.5 steps.
'use strict';
`;

  const output = `${header}
const GO_BASE_STATS_BY_DEX = {
${dexLines}
};

const GO_BASE_STATS_BY_NAME = {
${nameLines}
};

const CP_MULTIPLIERS = {
${cpmLines}
};

if (typeof module !== 'undefined') module.exports = { GO_BASE_STATS_BY_DEX, GO_BASE_STATS_BY_NAME, CP_MULTIPLIERS };
`;

  fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
  console.log(`\nWrote ${OUTPUT_PATH}`);

  // Quick spot checks
  const checks = [
    ['Venomoth',  49],
    ['Venonat',   48],
    ['Machop',    66],
    ['Snorlax',  143],
    ['Mewtwo',   150],
    ['Eevee',    133],
  ];
  console.log('\nSpot checks:');
  for (const [name, dex] of checks) {
    const d = byDex[dex];
    const n = byName[name.toLowerCase()];
    console.log(`  ${name} (dex ${dex}): byDex=${d ? JSON.stringify(d) : 'MISSING'}, byName=${n ? JSON.stringify(n) : 'MISSING'}`);
  }
  console.log('\nDone!');
}

main().catch(e => { console.error('Fatal:', e); process.exit(1); });
